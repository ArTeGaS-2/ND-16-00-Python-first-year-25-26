import turtle as t # Імпортуємо у файл і скорочуємо до "t"

# Налаштування пера
t.title("Квадрат") # Заголовко вікна
t.speed(6) # Швидкість руху пера
t.pensize(3) # Розмір пера
t.color("black") # Обираємо колір

# Малюємо квадрать 100x100 пікселів
for _ in range(4):
    t.forward(100) # Рух вперед у пікселях
    t.left(90) # Поворот у градусах

t.done()