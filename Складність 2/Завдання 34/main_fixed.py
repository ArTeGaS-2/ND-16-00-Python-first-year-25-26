"""Результат: програма має вивести найбільше число у списку, навіть якщо всі числа від'ємні."""

nums = [-5, -2, -8]

max_num = nums[0]
for n in nums:
    if n > max_num:
        max_num = n

print("Найбільше:", max_num)

