"""Результат: програма має коректно працювати, якщо введено не число."""

text = input("Введи число: ")
try:
    num = int(text)
    if num == 0:
        print("Ділити на нуль не можна")
    else:
        print("Поділене:", 10 / num)
except ValueError:
    print("Це не число")

